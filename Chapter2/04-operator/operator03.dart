void main() { 
  int num1 = 5;
  int num2 = 9;

  print('${num1} > ${num2} : ${num1 > num2}');
  print('${num1} < ${num2} : ${num1 < num2}');
  print('${num1} >= ${num2} : ${num1 >= num2}');
  print('${num1} <= ${num2} : ${num1 <= num2}');
  print('${num1} == ${num2} : ${num1 == num2}');
  print('${num1} != ${num2} : ${num1 != num2}');
}